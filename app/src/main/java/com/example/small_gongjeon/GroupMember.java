package com.example.small_gongjeon;

import android.graphics.drawable.Drawable;

public class GroupMember {
    private String name;
    private Drawable d;

    public void setName(String name) {
        this.name = name;
    }

    public void setD(Drawable d) {
        this.d = d;
    }

    public String getName() {
        return name;
    }

    public Drawable getD() {
        return d;
    }


}
